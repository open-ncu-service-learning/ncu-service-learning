<p> 
<h3>
   <li> 活動目的</li>
</h3>
<ul>

<li>響應世界地球日四十週年活動及本校所推動之「綠色啟動（Green Initiative）」計畫，強調綠色經濟和環境永續，愛護我們居住的環境和地球。建立學生社會服務的觀念，並能保持隨時為維護環境的奉獻理念。</li>
<li>引領學生對大自然有更深的認識，感受自然氣息的洗禮，增加大自然的體驗，以導覽解說方式加深對水域生態環境的認識，以及回收資源再生與再利用的宣導，藉以走出校園用不一樣的視野關心生態與環境。</li>
<li>同時藉由社區都市計畫與人文變遷，結合歷史、人文、生態、環境、資源利用等多元特色，強調自然環境與人為發展的平衡與取捨，促進學生關懷人文發展與生態保育的相互影響。</li>

</ul>
</p>

