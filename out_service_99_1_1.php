&nbsp;&nbsp;&nbsp;&nbsp;天文館將於10月16及17日辦理『打造星光小學，點亮星希望』活動，也讓全臺星光小學邁入第五所了!本活動目的是天文館結合社會資源，培訓偏遠地區學校校教師與學童，提升其專業的天文知識，讓師生成為天文科學的推手，整合當地天然優厚觀星環境，將天文科學教育深耕地方，打造成一所獨具特色的星光小學。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;星光小學第五所-臺東霧鹿國小位於莫拉克風災災區，為協助災區學校重建，並打造學校成為星空特色，由國立中央大學依「大專院校協助莫拉克風災災後社區重建服務計畫」辦理，中央大學初步擬定規劃災區重建點學校，以台東霧鹿國小實施中長期星光小學計畫，以落實教育部推動「大專院校協助莫拉克風災災後社區重建服務計畫」。本活動由中央大學率領服務團隊，偕同天文館科學志工共同執行，將於10月16日上午9點於天文館辦理開訓歡迎活動，並於10月17日下午14點結束後，由霧鹿國小學童布農族八部合音，歡迎民眾踴躍蒞臨同歡。


