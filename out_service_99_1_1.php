&nbsp;&nbsp;&nbsp;&nbsp;    如果你問兩天的星光小學活動學到什麼?我只能說：「星星!」
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;   小時候,我們認識的星星是楊桃的形狀,是圖形;然後我們學到天空中那一閃一閃的是星星、梵谷那迷人又炫目的星空,是一種發光的球體;陷在我從別人眼中看到的星星是自己!
<br/>&nbsp;&nbsp;&nbsp;&nbsp;    和小朋友見面的第一天,我只覺得「哇!一群小猩猩!」跑來跑去,動來動去,五十分鐘的課程對他們來說實在太長,但隨著一節一節的課程結束,小朋友的眼睛越來越亮,踴躍的舉手發言,不停的提問,有著我們這群大學生沒有的上課學習態度。
他們比城市小孩更活潑所以更需要關心,更努力學習所以更需要鼓勵。他們其實有著比我們更大的天空,只是他們不知道。不只是人類眼睛看出的視線範圍,這次從台東來台北看的是大城市的國際視野,在故宮看的歷史宏觀,在天文館看的是無垠的宇宙。
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;   從小我們學的公民只有責任與義務,規定要受教育可沒有要求要服務人群,然後自己也參加營隊被別人服務,現在是服務小朋友。當初,只是要分享小時候全家看流星雨的感動,那時無憂無慮,對於小小的我,天空是多麼的大。後來,因為要升學,看天空只有晴天和雨天的差別,哪有那閒情逸致看星星。大學後,一個人望著星空,是很美但有種孤單失落,「變縱有千種風情,更與何人說?」。直到,在星光小學跟夥伴們、小朋友一起學習,分享大家的感覺像油畫顏料一層一層疊起來,然後顏色更鮮豔,天空更多彩多茲;視線的距離越拉越遠,把大家看到的接起來,我們是在地球外看天空!
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;   不只是學看星星,學服務,大家總是說大學生都學非所用,怎麼會呢?學企管,用企管,管的不是企業,是營隊。大到時間流程,小到小孩的一舉一動。從人員工作分配、環境安全、活動設計等,都要管理啊!但更重要是管理自己,如何學,如何用。學的不只是天文知識,而是無垠的學海。學著要用不同方式,不同角度,透過不同經驗來遨遊宇宙般的知識。
 <br/>&nbsp;&nbsp;&nbsp;&nbsp;   從低頭看腳下,平視看遠方地平線,到抬頭看宇宙,學著看星星。星星不再是星星,你會發現我們大手拉小手,心中充滿熱情,然後我們也變成真正的星星了!


