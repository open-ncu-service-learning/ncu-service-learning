      <div class="TabbedPanelsContent">&nbsp;&nbsp;&nbsp;&nbsp;黑白分明的眼眸、天真無邪的笑容，一群來自台東縣霧鹿國小的布農族孩童，十月十六及十七日在中央大學課外活動組的悉心安排下，參與在台北市立天文館舉辦「打造星光小學、點亮星希望」活動。中大希望幫助原住民孩童打開宇宙知識之門，培育天文小尖兵，進而打造特色「星光小學」。這群孩童以天籟美聲「八部合音」表達由衷感謝。<br/>

&nbsp;&nbsp;&nbsp;&nbsp;中央大學響應教育部「大專青年重整校園送愛計畫」，從去年莫拉克風災之後，持續送愛到偏遠小學。「星光小學」的觸角已深入南投縣仁愛鄉合作國小、桃園縣復興鄉巴陵國小、台北市士林區平等國小、花蓮縣富里鄉東里國小、台東縣海端鄉霧鹿國小。服務的孩童不分族群，包括賽德克族、泰雅族、平埔族和布農族等。<br/>

&nbsp;&nbsp;&nbsp;&nbsp;中央大學學務長郝玲妮表示，中大的關懷重點並非是在第一線的緊急救助與安置，而是後續源源不絕的關懷活動，透過「教育」的力量，幫助受災孩童脫離貧困和弱勢，點燃新的希望。同時結合本校天文和科普科育等特色，利用當地天然環境及特有人文風俗，打造特色「星光小學」，創造偏遠小學特殊的教育價值。<br/>

&nbsp;&nbsp;&nbsp;&nbsp;參與此次志工服務的企管系莫雅如說，小六時父母曾帶著她觀看獅子座流星雨，美麗的景象，一直深植腦海，人類相對宇宙天文之浩瀚顯得渺小，因此特參與此次志工服務，大手牽著小手，一同滿足孩子對天文之渴望。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;她的觀察，原住民的孩童雖然學習資源貧瘠，但相對更珍惜所有，學習態度上十分認真。為期兩天的「童星飛翔，歡樂天文行」活動，帶領小朋友進行星座盤DIY、宇宙劇場、太陽系之旅、飛行DIY…等活動，原住民孩童都非常投入。寒假將再深入台東霧鹿國小，帶領全校一同觀星。偏遠山區因光害較少，是絕佳的觀星地點，加上原住民孩童對天象變化的敏銳度很高，多灌輸天文知識，有助他們成為天文小尖兵。<br/>

