								<p>
									<big><b>一、計畫緣起</b></big>
									<br><b>（一）緣起</b>
									<p style="text-indent: 30px;">本計畫規劃僑生或國際生返鄉服務，藉由在國內的服務培訓，培養學生參與活動時的應變組織能力以及領導力，並透過學生團隊（企劃發想、徵選、培訓、國內外服務及分享過程），來強化學生的志願服務知能，並具備服務前所需之技能，做為服務學習行前訓練，以期能培育出充滿創新創意、宏觀視野、企劃與執行兼俱的種子人才。</p> 
									<p style="text-indent: 30px;">因此，本組為培養以東南亞國家為服務據點的國際志工服務學習團隊，給予僑生或國際生回到自己的家鄉服務回饋社會的機會，於2017年底成立「中大國際服務學習志工團隊」。有別以往於往年所招募的任務型國際志工團隊，任務結束即告解散。本團隊是為有組織性、制度性之團隊，參與國內、外服務，並希望藉由規劃國際服務學習志工團隊，為本校注入更豐富的元素，進而發展與國際接軌的力量。</p>
									<p style="text-indent: 30px;">為加強學生團隊的服務技能，並培養學生對東南亞人文風情的了解，故在國外服務前，先行在國內從事相關服務及一系列的培訓，提供學生參與本土文化及新住民文化之服務機會，也讓團員走出生活舒適圈，親自參與服務執行，嘗試貢獻己力協助他人，透過原始感官發現真實世界，從中重新審視自我定位，樂在服務享受學習；同時以座談會或檢討會方式進行知識移轉與分享，加強培養反思帶領者，訓練學生帶領反思的方法與技巧，並持續反思，以提昇服務學習的效果。</p>
									<b>（二）國情地理環境簡介</b><img src="images/oversea/viet_map.png" style="float: right; margin: 15px" width="40%">
									<p style="text-indent: 30px;">越南社會主義共和國（Cộng hòa Xãhội Chủ nghĩa Việt Nam／Socialist Republic of Vietnam），通稱越南，位於東南亞中南半島東端，與柬埔寨、寮國和中國的雲南省及廣西壯族自治區接壤。</p>
									<p style="text-indent: 30px;">越南目前由越南共產黨執政。越共是越南唯一的執政黨，對越南行政、立法、司法各個部門的機構直接掌控，並由越共黨員擔任各部門的政務職位。</p>
									<p style="text-indent: 30px;">越南全國大約 331,051平方公里。地形包括有丘陵和茂密的森林，平地面積不超過 20%。北部地區由高原和紅河三角洲組成。東部分割成沿海低地、長山山脈及高地，以及湄公河三角洲。</p>
									<p style="text-indent: 30px;">氣候屬熱帶季風氣候，濕度常年平均為84%左右。年降雨量從120厘米到300 厘米不等，年氣溫介乎5到37攝氏溫度之間。</p>
									<p style="text-indent: 30px;">越南總人口數為9,171萬餘人（2015），境內有 54 個民族，主體民族京族占總人口 87%，其餘為岱依族、傣族、芒族、高棉族、華族等少數民族；華人人數為823,071人（佔越南總人口 0.96%）；按各民族人口數量排序，華人是越南第八大民族。</p>
									<p style="text-indent: 30px;">而服務據點同塔省位於南越，區屬湄公河三角洲，北與柬埔寨相鄰，南接芹苴市、永隆省，西鄰安江省，東鄰隆安省、前江省，與胡志明市距離約145公里。</p>
									<big><b>二、計畫目的</b></big><br>
									<b>（一）培養學生國際視野及對於人道事務的關懷</b>
									<p style="text-indent: 30px;">鼓勵學生走出熟悉的小世界，將在國外所見所聞與自己的專長結合，並在舞台上展現出來與大家分享同時也增強自我信心及勇氣，在海外服務也提供學生人道關懷的概念，用這些經歷來警惕自己不再只是關心與自身相關的事物，將國際志工當成一個起點，開始關心社會及世界議題。</p>
									<b>（二）推廣教育，增加學習動機</b>
									<p style="text-indent: 30px;">因當地教育資源不足，利用本校學生們所準備正規課程結合團康活動來增進學童們的學習動機及課外的多元學習課程，如：美術、舞蹈等，拓展當地學童之學習觸角，使其擁有豐碩的學習成果。</p>
									<b>（三）學習尊重異文化並相互交流</b>
									<p style="text-indent: 30px;">與我國不同的海外文化是需要學習的，透過於當地的食衣住行來包容與自我不同的個體及習慣，學習對於彼此的尊重，亦可進一步的互相交流，將我國的文化與當地居民分享，互相學習，促進彼此成長的動力。</p>
									<b>（四）推展社區發展及環境美化</b>
									<p style="margin-left: 30px;">1、落實居民參與環境資源保育共生計畫，藉由在地人的投入，協助推動資源保育工作，宣導綠美化教育及協助環境綠美化維護。
									<br>2、協助校區進行，校舍油漆及教育設備改善，期待可以將校園的環境美化，相信整潔明亮的學習環境可以幫助提升學童的學習動機。</p>
									<big><b>三、計畫內容</b></big>
									<br><b>（一）主辦單位：</b>國立中央大學。
									<br><b>（二）活動地點：</b>Sa Dec City, Dong Thap（同塔省沙瀝市）, Vietnam.
									<br><b>（三）服務對象：</b>當地學童、青年及社區居民。
									<br><b>（四）計畫時間：</b>2019/08/05～08/16（暫訂）。
									<br><b>（五）培訓時間：</b>詳見培訓課程。
									<br><b>（六）服務人數：</b>15人。
									<br><b>（七）計畫內容：</b>
									<p style="text-indent: 30px;">地處南越，被視為湄公河三角洲的最大花卉產地，位在湄公河畔的SaDec City，以盛產花卉、懂植栽的花農聞名；觀光與園藝花卉是當地重要的經濟來源，掩藏在快速經濟發展下，貧富不均是當地的現況。因此，十年樹木、百年樹人，教育是長久大計，也是新一代轉未來的重要武器。本志工團隊對資源弱勢兒童成長的教育協助與傳達關愛，辦理各項教育活動，期能透過永續的關懷行動，協助他們快樂成長。同時，結合近年來國際先進社會及臺灣政府力倡服務學習、志願工作的面向，以及滿足世界弱勢區域的需求、符應聯合國千禧年發展目標，持績性地籌組帶領我社會青年往世界需要的角落去，絕是一項利己利人的世界公民義務。</p>
									<p style="text-indent: 30px;">同時，為推廣並美化當地環境，本團隊希冀透過社區民眾的合作，讓環保走進每個人的生活，強化居民的環境意識和文明素質，推動大眾對環保的參與。同時，在推動綠色社區的過程中，藉著各項活動，增強社區居民的凝聚力，創造出一種與環境友好、鄰里親密、和睦相處的社區氛圍。</p>
									<p style="text-indent: 30px;">我們企盼：藉著實地走訪與教育實務的投入，將帶給世界偏遠角落中孩童，難以取代的實質關懷和希望。因此，本團隊擬進行服務活動之內容如下：</p>
									<p style="margin-left: 30px;">
									<b><u>1、清理社區內街道容貌，社區入口意象植栽：</u></b>
									<br>（1）依社區實際需求，提供居民需要的勞力協助：預訂項目包含清理雜亂的樹枝及美化街道，並與當地居民沿著街道種植更多的花。
									<br>（2）走進社區拜訪家庭，深入了解社區居民的生活、越南文化和社會現況，同時也感受志工在社區所創造的改變。
									<br><b><u>2、學校空間彩繪：</u></b>
									<br>結合當地學校需求及風俗文化，彩繪牆面增添校園活潑氣氛，並推廣本校知名度。
									<br><b><u>3、國小及學齡前兒童陪伴：</u></b>
									<br>（1）安排傳統藝術活動：讓當地學童認識台灣的文化，例如音樂或表演節目；希望透過此課程讓學童喜歡台灣文化，也能體驗不同學習方法的樂趣。
									<br>（2）由學生自行規劃團康、舞蹈等活動，透過遊戲、音樂及舞蹈，讓當地學童與本國學生融入無國界的生活，學習互助合作的概念、激發潛能，並從中體驗學習的樂趣。
									<br>（3）規劃多元英語學習課程與活動進行英文教學，運用歌曲、戲劇及遊戲等寓教於樂的方式，將英語生活化並極富趣味性，激發孩子對學習的樂趣，提升學生英語學習動機
									與能力。
									<br><b><u>4、文化交流：</u></b>
									<br>（1）與當地居民一起進行常態【花田】種植養護工作、深入在地生活體驗。
									<br>（2）服務期間不但生活在服務社區，增加志工與社區互動，同時也有越南志工參與，與越南大學生做朋友，讓台越交流零距離！
									<br>（3）安排工作坊以提升當地居民有關觀光景點環境問題的意識。
									<br>（4）安排社區居民與本團隊志工交流彼此文化，如風味飲食、傳統服裝、小遊戲等。
									</p>
