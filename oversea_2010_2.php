		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;有許久不曾細細地省思、沉澱自我，並將自己內在無形的思想與信念轉化成有形的文字留下成長的痕跡，對於自己所書、欲留下跡痕的文字我總是特別苛刻而要求的，從不奢求能沾上文學殿堂的邊緣，縱使只是小小一隅，但對於文字，我總有著深刻近乎虔誠的敬重，於是因為自己胡地加諸的沉重包袱，有時我會開始逃避，逃避提筆、逃避寫下自己的成長與感受，而往往又正因為不願面對，我遺失了許多瑣碎卻有著深刻意義的點滴，這趟擔任國際志工前往雲南麗江服務─不同於往常出走的旅行，是該留下些許的痕跡。回國後已近一週的時間，在慵懶怠惰與繼續生活的疲憊之餘，重整並留下探索的行跡亦或是對自己不可推卸的責任吧！
		<br>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;回顧過往一個月來行前訓練、準備以及服務所經歷的一切，或許，今日我所記錄下的早已非最真切的事實與景象，而是摻雜了許多自我意念的綜合體，也或許，在書寫的當下，我會不期然而然地將現今已成長、蛻變的我置放在回憶的時空中，以今日的思想取代了當下最直接的衝擊與感受，但這也可算是另一種貼近真實的成長記錄吧！因為人本是層層疊加的混融，不論是過往還是在此書寫的片刻，我，還是我。寫下的或非藻麗的文字，或亦無精煉的思想，只期望在望過、走過、經歷過後，我猶能以澄澈、赤子的瞳目回首所歷之斑斑行跡與蛻變的自己，然能寫出的、道說的總比不能書寫訴說的少的多，記錄或也只是種貪痴的奢求。
		<br>
		&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;參與這次國際志工服務的活動，起初我從未深刻地思考自己欲出走的理由，只覺得冥冥中有股力量不斷地推著我往前走，行前訓練，我帶著滿腔的熱情和傻勁欲衝、欲闖、欲展現自我，受訓後，我的身心反蒼老憔悴了許多，為何我想要當志工？我是真誠地為了服務他人還是帶著點私欲欲證明自己或從其中得到更多的收穫？為何單純的服務需跑到國境之外的雲南，身在家鄉不也能付出自我、貢獻己力嗎？短短不到一個月的時間我到那兒能夠做些什麼？或許一切都是個零，什麼都沒有。眾多的疑惑如浪潮一波波朝我襲捲而來，撞擊心靈後，靈魂的岸礁被打得坑坑洞洞，白沫飛濺的浪花並無消失而是噴舞衝擊了更多更多……。有時，閱讀真是種救贖，閱讀在越緬邊境長期擔任志工的賴樹盛所著之《邊境漂流》後，我隱約找到了方向，減緩紓解了消極、茫然的無力感，「因為擁有得以思念的故鄉以及追逐的夢想，才使我勇於遠走他方。」「如果當下的苦難是不能改變的，也許我們就只能拼了命從中學習，以期在往後開創出什麼來。」，稍稍舒緩了身心的疲憊，出國服務之期近在眼前，不管自己準備地如何七零八落，來得及的與來不及的一切都由不得我誤點，只能準時啟航。
