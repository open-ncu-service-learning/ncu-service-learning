			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;如何能夠在世態炎涼的社會中生存，並且在求取功名時能夠具備悲天憫人的情懷，以及回饋社會的感恩之心。」這是我大學四年一直反覆琢磨的一件事情，難道所謂的成功就只是財富資源的重新分配嗎？沒有任何的一絲的感性、人性以及補償的機制在裡面嗎？很高興在這次活動的過程中，我找到了一把鑰匙，逐漸地解開了這個問題的關鍵，也讓我未來更有信心的持續走下去。
<br>			
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;營前訓出發前的營前訓讓我思考了很多，心態也調適了很多，為期約一個禮拜的課程中，每一堂課都讓我印象深刻，也讓我具備了更多的服務工具並且讓我更了解自己。其中，李柏賢老師利用簡單的團體活動讓我了解到什麼是「團隊合作」，如何跟別人有良好的溝通，這比任何管理書籍中看到的文字都來的真實並且無疑是個當頭棒喝；此外，陳建松老師讓我了解到當一位企業家和志工活動是沒有衝突，甚至是相輔相成的，這也讓學商的我吃了一顆定心丸，也讓我了解到自己的使命和未來在哪裡。還有許多老師實務上的志工經驗分享，都帶給我不少的成長、啟發和突破。
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由於時間的緊湊和不確定性，準備的時間非常有限，然而靠著團結合作和大家豐富的辦活動經驗，我們的活動以極高的效率產生，並且在驗收時都能夠有良好的效果，身為活動長的我真的很謝謝大家的配合，更謝謝我的另外一位搭擋─吳佩雯同學的付出，讓整個志工活動能夠如期順利成行。
<br>
<strong>雲南麗江的生活體會(官方版)</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;麗江海拔約2600公尺，四季如春，空氣清新宜人，好山、好水和好空氣讓當地的居民非常長壽，是中國蠻長壽的地區，不過罹患青光的眼機率也非常高(紫外線)。一下飛機，我們馬上就感受到強烈的紫外線，不得不拿出太陽眼鏡和防曬乳以免曬傷；居住上台聯大安排的還不錯，和台灣相距不大；飲食上雲南重鹹重辣是其特色，雖然我不喜歡鹹但是我非常喜歡辣，所以我很適應當地的飲食，不過環境不是非常衛生，蒼蠅非常的多，待到最後感覺腸胃不是已經成為一種常態；居民方面，純樸老實易於相處，雖然麗江算是鄉村，沒有像都市人的光鮮亮麗，但他們努力勤懇認份，也令我格外欣賞。麗江的觀光非常著名，玉龍雪山雲霧繚繞，具有壯麗和神祕之美，除此之外，他們將過去的古城區和現代商業結合，吸引許多的國外觀光客到此遊玩，在麗江古城內，可以品嘗道地的普洱茶、胖金妹牦牛肉乾、體會納西族的歌舞，是一個非常特別的是覺和味覺饗宴，蠻令人陶醉的。
<br>
<strong>雲南麗江的生活體會</strong>(朝偉田野調查版)<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;麗在江這幾天，每天當志工結束候，我總是約三五好友在麗江到處田野調查，我們不去一般的旅遊景點，反而走往郊區、鄉下，利用和當地人互動和溝通，也讓我對真實的麗江風情也體會了不少。
我認為要了解一個地方的文化並不是觀光區，而是當地的菜市場，因為古城觀光區是主要是由沿海的福建廣東人經營，已經和過去的麗江文化脫節許多。麗江的菜市場主要是販賣當季盛產的桃子、李子和梨子為主，由於高原地區盛產馬鈴薯和玉米，地上成堆的馬鈴薯和玉米蔚為奇觀，他們至今仍利用傳統的桿秤完成交易；水果食物方面，物價相當的低廉，當地納西族為多數，老一輩的他們仍然使用納西話溝通，使用普通話的我們相對非常吃力，走入菜市場才能夠深刻了解少數民族他們的生活型態和方式，這是一個很難得的體驗。
在一次的田野調查中，我們走入了雲南白族的村落，跟當地的老先生和老太太溝通，當地的老先生非常熱情，帶我們參觀白族的擺設，例如像是豬舍以及碾米廠等等，讓我們體會他們傳統的生活方式。我自己也走入了比人還高的玉米田中，和當地的白族小孩玩耍嬉戲，幫他們摘蘋果，和當地小孩有良好的互動，真是令我印象深刻。
<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;另外一次的田野調查中，我們幾個男生，包了一台吉普車，前往中國的馬鈴薯故鄉─太安，和當地的納西族人共進早餐，並且下鄉參與他們的農事，幫他們做油菜花的二次施肥，滿山滿谷的馬鈴薯花，白色的花代表莖的顏色是白色，紫色的花代表莖的顏色是紫色，再加上黃色的油菜花，構成了地表一塊顏色鮮麗的地毯，令我目不暇給。看到他們的生活，或許沒有我們都市孩子光鮮亮麗的外表，以及豐富的教育和社會資源，但他們自給自足，從他們因陽光而曬成黃褐色的臉上，我看到了純樸、自然和幸福，在親自參與農事中我深刻體會到「粒粒皆辛苦」的真正涵義，我也提醒自己要好好珍惜自己現在的生活，珍惜食物，也提醒自己未來有能力時要多多關心這些在基礎產業上默默耕耘的人們。
<br>
<strong>丫丫幼兒園</strong><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由於幼稚園的小朋友需要適當地控制秩序以利活動的進行，因此身為活動長的我規劃活動之餘，在丫丫幼兒園主要是擔任值星官的角色。值星官對我來說是一個相當大的挑戰，因為我必須以身作則並且不苟言笑，在營期過程不可以和小朋友互動，必須要擔任一個黑臉的角色，為了這個角色，我必須改變自己隨和的處事態度並且收斂自己的笑容，讓小朋友對我產生敬畏之心，直到最後一天的破值星才可以和小朋友親近，不過正是因為這個角色，我可以很清楚的掌握全部小朋友的學習秩序和狀況。有的小朋友活潑好動，雖然行為舉止難以約束，但是他們總是班上領導者，會幫助同學解決許多問題；有的小朋友雖然剛毅木訥、沉默寡言，但是在勞作和說故事上，他們總是有細心的傑出表現。其實每一位小朋友都不一樣，都有它的優點和缺點，重要的是為人師者要如何去引導孩子走向正確的路，這過程除了愛心和耐心，別無他法。而我們一舉一動都會對他們在幼年有深刻的影響，因此我們必須非常注意自己的教學方式，這中間牽涉到城鄉間的價值觀以及兩岸不同的文化差異，這些我們都是我們在教學上必須注意的地方，這些過程是我在這次志工活動中體會很深刻的地方。
<br>
<strong>小百花幼兒園</strong> <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;小百花幼兒園位在麗江的郊區，比起丫丫幼兒園，小百花的環境相對比較原始並且自然，裡面的小朋友少了城市小朋友的伶牙俐嘴，卻多了一分純樸和可愛，我在這裡主要是擔任教學的工作，一開始由於這邊的小朋友主要是白族和納西族，語言和文化的差異，溝通並不是非常順利，需要靠動作引導以及學校老師的輔助，但是小朋友進步的很快，隔天就有良好的互動和進展，比起市區的小朋友，小百花小朋友的精力和學習能力一點都不輸給他們。每次看到小朋友參與活動露出來笑容，我總是非常感動，這就是教育呀！一代接一代，我們小時候也曾經歷過那一段幼兒的歲月，如今我們都已經成年，成為社會上的主力，過去講台下的我如今也站在講台上，把過去社會給予我們的一切回饋給他們，我想這種傳承、這種收穫、這種感觸就是國際志工的真諦吧！

