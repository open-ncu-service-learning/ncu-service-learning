<p>
<h3>
    <li>緣起</li>
</h3>

<div>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;因地緣關係，國立中央大學位於全台新移民與外籍配偶眾多的桃園縣，在這個充滿東南亞風情的國際航空城，桃園縣政府扶植成立新移民學習中心，坐落於外籍第二代最多的忠貞國小校區，2006年開幕當日邀請本校僑生前往表演緬甸傳統舞蹈，有著共同文化背景的外籍配偶與僑生有了第一次的相見歡。爾後中大僑生邀請新移民中心成員其子女來校參觀本校太空遙測中心，並在2007年成立《松果學習營隊》，於本校5月「愛月」的母親節，為新移民中心家庭舉辦親子一日營活動，獲得熱烈迴響。<br>
隨後忠貞國小通過「教育部推動大陸籍外籍配偶子女教育輔導計畫」，其中「繪本親子共讀活動實施計畫」，因著僑生一定的學識與兼惠東南亞各地區語言，因此由中大僑生協助執行，進而成立《Q Pine讀本隊》。2008年《Q Pine讀本隊》第二度獲邀執行桃園縣忠貞國小「繪本親子共讀活動實施計畫」，並於年底與本校諮商中心合辦《環保童話營》，合作對象為桃園縣忠貞國小與內壢國小，前往永安漁港淨灘。
<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2008年，僑委會舉辦僑生保送推薦委員考察團來中央訪問，來自印尼印尼泗水台灣學校（Surabaya Taipei International School）的蔡先口校長於會中提及印尼的華文教育困境，激起多為印尼僑生的讀本隊同學返鄉服務的熱情，同時響應教育部、僑委會鼓勵國內大專校院學生參與國際志工服務，培養學生對國際社會人文關懷之使命感、責任感，拓展國際視野履行世界公民之義務，中大《Q Pine讀本隊》計畫至該校輔導幼稚園與國小學童認識華語正體字、國語注音符號等，及口語交談。且以國語日報、兒童日報，或台灣的報紙內容為主要教材；以期他們將來可以來台灣升學、早日融入台灣的社會。
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一所一流的國際大學，除了在學術發研究上有傲人的發展與成就之外，擁有具備國際視野的莘莘學子也是不可或缺的。透過團隊活動提高學生的惻隱與關懷社會之心，並與國際社會交流，進而提升了中央大學的國際能見度。期望與印尼泗水台灣學校合作，安排志工實地前往，讓台灣青年透過志工服務協助推動僑校業務，並促進台僑青年互動，從服務中獲得學習的意義。更重要的是，藉由中大人的服務，向國際社會展現台灣人的熱情與愛心，更能達到一次實質外交的貢獻。

</div>
</p>


<p> 
<h3>
   <li>活動目的</li>
</h3>
   
   <font size=small>
   
   <ul>
                 <li> 鼓勵學生利用每年返回僑居地的機會，安排週間時間輔導當地華僑學童，協助課業學習、文化認識、增加生活技能，以教育給予希望，以技能提升生活水準 </li>
                 <li>協助印尼泗水台灣學校華文教師，透過活潑有趣的主題，提高學童對認識中華文化及學習華文的興趣。藉由介紹台灣節慶、兒童繪本導讀，讓華僑學童增進國際視野，學習不同文化內涵，同時透過DIY及遊戲，讓學童能親手獲得實質回饋，提高學習意願與成就感</li>
                 <li>將「服務學習」之內涵與精神融入，正面思考生命的意義與存在的價值，培養樂觀進取、積極奉獻及關愛國際社會的服務人生觀</li>
                 <li> 藉著與國際青年合作交流的機會，建立開拓的國際觀與國際人脈，並將參與國際志工服務的收穫與經驗，回饋中央校園與台灣社會；藉由校園分享活動為青年的生命教育，開創新的議題</li>
				 <li>建立全民參與志工服務與全民外交的機會，拓展台灣能見度及中大學生國際視野，提升台灣的國際形象，與世界接軌</li>
				 </ul>
   </font>
   </P>


