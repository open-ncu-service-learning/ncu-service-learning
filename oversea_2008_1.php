　　第二個禮拜服務的地方是南孟買，這裡是市中心也就是台北的信義區！我們住的地方是六星級飯店旁邊的背包客之家Savation Army，這是一棟看起來歷史非常攸久的旅舍，班駁的牆壁破碎的窗戶真的很像電影裡的場景！這禮拜服務的地方需要坐公車才可以到，這裡是垂死之家的分支機構Asha Daan（上帝的禮物）。這裡有六個病房，第一間是兒童病房大約收留一百多位的兒童病患，有腦麻、肢體殘缺、唐氏症、蒙古症等…身體不正常的小孩。第二間是女病房裡面有一百五十位左右的女病患，他們或坐或臥的躺在地上無助的望著前方，好像不知道在等什麼的樣子，這裡的空氣混雜著排泄物和消毒水的味道，不通風的環境讓人覺得很不舒服。第三間是女精神病房，病房內大約二十來位的女精神病患他們也都很自得其樂玩的很開心。第四間是男病房，這是最大間的病房裡面有兩百多個病患，這裡環境比較通風所以不像女病房有那麼難聞的味道。穿過一個鐵門後面還有一間小小的病房，撲鼻而來的是濃濃的消毒水味，這裡是愛滋病房。裡面大約有十幾位的愛滋病患，坐在病房外有一位年輕的女孩很熱情的跟我們打招呼，他的英文很流利人也很親切。最後面的是街童病房，這裡的小孩都是學齡小孩，但是因為他的身體的殘缺障礙，讓他們不能和正常小孩一樣去學校上課。
　　我被分配到女病房去幫忙，首先是分送食物，病患們爭先恐後的和我們要食物，讓我們非常的手忙腳亂…分完食物後是餵食，我餵的那位病患他眼睛看不見，不會說話手也都變形了無法自己拿東西，餵他的時候要小心的把東西都切碎磨的爛爛的給他吃，因為他沒有牙齒。吃完飯後，修女要我們拿指甲剪來幫他們剪指甲。一開始我真的很排斥，因為左手擦屁屁右手吃飯飯的印度手指甲縫中，真的充滿了很多的汙垢…。有空的時候我會去兒童病房幫忙，裡面的小孩真的很調皮，其中有一位小公主，他眼睛大大的但是他的脊椎有問題所以不能像其他小孩一樣站起來或做起來要人家抱，他每次都自己躺在常上看別人玩，但他一直在笑，他的笑容真的很像天使…所以我每次去都會去抱著他到處散步，有時他累了也會很安心的在我懷中睡著，有時候，女病房須要幫忙我都很捨不得把他放回床上去。在Asha Daan服務的倒數第二天，我們幾個人去找愛滋病房的Sabina，他是一位十九歲女生，兩年前被送到這裡。他很無奈的說：「是我姊姊送我來的，我也不想待在這但是又能怎麼辦呢？」他是這裡少數有受過教育的人，但是這個病就像是宣判他死刑一樣，讓他十九歲的年輕生命終身監禁在這個地方…但是，他總是帶著燦爛的笑容和我們聊天，他很勇敢但他的堅強卻讓人看了心疼…。
　　在印度的這兩個禮拜，我看了很多得到了很多學習了很多。最讓我印象深刻的是在Asha Daan的服務，由於接觸到的東西是在台灣很難遇見的，心情上的調適和轉換，從很不能適應到最後很捨不得離開他們。在Asha Daan我看見了天使的笑容！另一個衝擊是印度的貧富差距，高級的泰姬瑪哈酒店v.s.滿街上的遊民；印度的豐富宗教文化，各個教徒的特別裝扮彩繪了印度的街頭；印度特殊的人文風情，哄抬價錢的計程車司機v.s.善良的好心路人。太多太多的衝突太多太多的矛盾胎多太多的文化，是讓印度如此精彩豐富的原因＂incredible India＂印度真的是個不可思議的地方！

								
