								去年參與的泰緬服務團隊，也是中大第一次辦理海外活動，讓我在海外見識並學習到許多，今年又希望能讓更多人有這個機會參與，增進中大同學的國際觀。由於去年我有了第一次的經驗，就接了本次活動的總召，從一開始不斷的蒐集資料、找尋網路上可服務的據點，到後來對於學校經費的審核補助，團員間的招募，又因牽涉到對印度情況的陌生及各種的個人因素，可能是家裡或是經濟上，甚至是家長對於安全性的質疑，使得在整個過程中我遇到不少挫折。
    到了出發前夕，一切塵埃落定，才開始對印度－自助旅行中的最高級班(初級班是香港及日本)，想要一探究竟，一下飛機後就開始用眼睛不斷的去觀察，去了解這個國度的各種現象，畢竟這裡是──Incredible India，真的任何事情都有可能發生。
    第一個禮拜是在安貧小姐妹會的安養院服務，我被分配到擦輪椅的工作，坦白講他們的環境設備算是相當的不錯，做的只不過是他們平常比較不會去碰觸的部分，相當於去減輕修女的工作量，其他人的分到有擦窗戶、擦輪椅、搬運物資、在廚房幫忙切菜剝蒜頭、與老人家聊天……等等。這樣的工作從每天的早上到中午，持續了五天，這當中特別值得紀念的是在最後一天服務的早上，由於我們的上一梯Ａ團有準備了小型的感恩晚會給安養院的老人們，所以在這梯姜老師也希望我們能出些活動，中大同學是表演帶動跳－妳是我的花朵，非常具有台灣味的音樂，但還加入了一些印度舞的舞步，但畢竟是臨時才要求的，所以精緻度就不是那麼高啦。此外，我solo了一段在社團期間學的棍舞表演，但音樂在播放時有跳針，沒有準確的抓到節拍，有點小遺憾。睿哲(全程參與ABC三團的姜老師助手)說道：你們Ｂ團已經表現的很不錯了，因為上禮拜Ａ團的晚會表演時，都要自己人先拍手鼓掌，老人們才會跟著有些稀疏的掌聲，而這次我們的表演結束後，則是老人們主動為我們鼓掌。姜老師也說我們表演的已經很夠了，觀察到老人們都很開心。聽到他們這樣說就覺得很值得。在表演的最後，有個貼愛心小卡的活動，想法是來自聖約翰科技大學的同學，可以在這幾天服務以來，貼愛心在與自己比較有接觸到的老人們身上，愛心小卡可以寫上對他們的祝福，團員中的幾位及一些老人們在拍完照之後情緒都顯得有些激動，有些老人們會用不太流利的英文一直說一定要回來看他們等等之類的話。雖然在這邊服務的一個禮拜不長，但卻與他們有建立些感情。
    第二個禮拜是前往南孟買，這邊算是孟買的精華地區，有許多的觀光景點都在此，觀光區的英文程度明顯比北孟好的太多了，但印式英文真的比日式英文還難理解……,這個禮拜則是要去德雷莎修女的垂死之家在孟買的機構- Asha Daan 服務，跟安養院很不同的是－這邊收容了各種的傷患，分成男病房、女病房、男精神病房、女精神病房、愛滋病房、兒童病房，精神病房跟愛滋病房是不能進去的，男生不能進女病房，反之女生亦然。服務內容中比較特殊的是為傷者換藥，在來之前就有聽到Ａ團的人說換藥組必須要做好心理準備，由於本人愛嘗鮮加上膽子又大，我與團員囷羽就選擇了換藥組，因為他們是八點開始換藥，加上要搭公車才能到達，就持續一個禮拜六點半起床，第一天因為是團體行動，抵達時剛好到換藥時間的末端，只剩下最後的幾個病人，但光是看那幾個病人的傷口，著實讓我震驚到，畢竟傷口不是深可見骨，而是骨頭就露出來給你看的一清二楚，想到明天開始就要上場了，對完全沒有醫療背景的我產生些許的不安，但還是馬上恢復我在國外的觀念：少問、多觀察、多想。當天下午晚上也是跟平常一樣，在孟買市區探索並吃吃喝喝，晚上無所事事就早早入睡了。隔天到了換藥的男病房，戴上從學校衛保組提供的口罩、手套及圍裙，平常換藥的是一個男護士及一個修女，病人們就排排的做好等換藥，其實所謂的換藥，每個人的步驟其實都一樣，一開始一手拿剪刀夾，另一手拿剪刀把覆蓋在傷口上的紗布用剪刀剪開，將紗布及覆蓋的棉花移除並露出傷口，用剪刀夾夾棉花浸消毒水後清洗傷口，常常傷口附近都有些黃色的膿，能推掉就盡量推掉，但過程常會聽到病人的哀嚎聲吟，清洗後用乾棉花將傷口做乾燥，另外拿一塊薄薄的纖維，塗上些碘酒藥膏並完全覆蓋在傷口上，傷口越大就要用越多，之後覆蓋上一塊棉花並用膠布固定就完成換藥。但每個人受的傷都不相同，像有個老人手臂上有個小膠布，剪開後有一個洞，一握其手臂就會開始流出液體撞的膿，老人駕會開始哀嚎，說真的心裡也不太好受，但又非得將膿水擠出不可，就只好一兩人抓住他，其他人開始擠，看到擠膿可以擠出一整碗的……，帶著口罩也可以聞到濃水的味道。像還有個傷患是在小腿腳踝的部分，發現那邊缺了一塊肉，凹進去直接露出骨頭，不是深可見骨，而是骨頭就已經露出來在你面前，而且還是斷骨，另一半還在腳跟，骨頭旁邊就有膿、血水、殘缺的肌肉組織，還記得我在消毒他傷口時，棉花碰其斷骨，骨頭就在我面前晃阿晃的，我也不禁吞了好幾下口水……；還有像是頭骨缺了一塊，頭頂上只有薄薄的一層皮，隔著皮就看的到腦；或是腳上大拇指斷掉，只留下一塊白色的腳指骨，周遭都是發黑的肌肉組織……換藥過程中空氣瀰漫著消毒水、些許霉味及尿騷味、碘酒及膿水的味道，真的不太舒服。
    第一天硬著頭皮上場，等換藥結束後，在一個小房間休息，兩眼開始放空，畢竟在過程中神經都很緊繃又要克服心理壓力，換藥結束後其他團員問我今天的心得是甚麼?　我只回答說我的層度又提升了…… 。心中更佩服德雷莎修女的偉大。換藥到了第二三天之後，對每個人的傷口都已經有個底，就比較習慣了。我們在換藥結束，彼此聊聊天交換心得，常會出現個問題，他們到底是怎麼受傷的? 怎麼可以傷的如此嚴重而不做治療? 大家也討論不出個甚麼結論，不禁讓我想把這個答案找出來，因為在國外跟在國內不同，很多事情的原因都要自己去找尋，甚至沒有甚麼正確解答，但喜歡這種正確解答的心態正是台灣學生在考試制度下所養成的，期望別人告訴你答案的心態。而這個道理是去年我去泰緬，當地的領隊SAM所教導的。我發現一個現象或許可以解釋：在那邊換藥是每天的工作，除了禮拜四之外，他們是禮拜四公休，我在禮拜一到三的連續的三天，都幫同一個老先生換藥，他的傷是胸口缺了一塊皮，露出的肌肉組織感染化膿成一整片，膿水在沉積在肩胛骨的凹處，大概十乘十公分的大小吧，但由於禮拜四公休，就那天沒換藥而已，禮拜五再去幫他換藥的時候，發現除了原本的傷口之外，化膿區又更加擴大，非常的明顯，以他的例子來說，我猜測原本他們可能因為意外受了點小傷，但是由於貧窮或者其他原因，而沒有就醫及早治療，小傷口被細菌持續感染，才會那麼怵目驚心，從一個禮拜的觀察下來，這是我自己的想法推測，但是否真的如我所想的，就要更長的時間或成為終身服務者才有辦法真正的了解吧。
    在換完藥及小休息之後，男病房就只需要等到中午時遞飯就結束了。所謂的遞飯就是把中午的每份餐點 ”照順序”的發放給每個人，會刻意強調照順序是因為在裡面也有階級之分，因為一個禮拜下來的發放順序都一樣，第一個吃飯的是個瘦小戴眼鏡的老先生，我都稱呼他為元老，應該是地位最高的。在換藥及遞飯的空檔，就去兒童病房，大概是幼稚園跟小學的年齡，我們都稱之為天堂，因為相較其他病房，這間的氣氛格外的輕鬆歡樂，即使小朋友們身體有所缺陷，還是覺得充滿希望，在這間的工作不外乎就是陪小孩玩樂、分類其衣服、以及餵食。由於有些小朋友年紀太小或是精神上有缺陷不能自己吃飯，就要輔助餵食，用湯匙將一盤粥狀物餵完，但依照每個小朋友的配合度不同，所需的時間也差很多，從半小時到一個半小時都有……在考驗每個人的耐心，有餵過一個小朋友，湯匙到嘴邊就是堅持不肯張開，不然就只是張開嘴唇但牙齒還是緊閉著，一閉起來就流的滿身都是，只好請修女幫忙啦!
    在餵完飯之後，有小朋友就會睡覺去了，在那邊的好幾天，我常常會在他們的床邊，摸著他們的小臉，心裡一直在想：如果我是他，出生就被送來這邊，就沒有父母，在這邊長大，可能也沒有受教育的機會，小傢伙阿……你們的未來到底在哪邊呢? 在我們回去之後，你們是否還會對我們留有一絲絲的記憶呢……不斷的自我對話，但又無法有解決的辦法，我想這是很多人道工作者常換面臨到的問題吧……
    其實兩個禮拜的短期服務所能做的有限，甚至可以說很少，在去年就體會到這個道理，而短期服務的目的我想就是在於－回國之後，盡量把當地的情況，多跟親朋好友分享，讓更多人知道當地的情況，這樣就很足夠了，想並不需要說一定要幫他們募集到大量物資或是設法跟大企業拉到甚麼贊助，畢竟一個「平凡」人的力量很有限，身上並無光環，而且也不切實際，但如果真的有人這麼去做的話，我會由衷的佩服。我認為應該是把這份服務的心，長久的在心裡保留下去，在未來自己有更多的經濟能力時，即使沒有時間，但也可以提供長時間的資金援助。我想這就是短期國際志工的價值所在吧!去年去泰緬，當地的領隊－賴樹盛　SAM大哥 在今年六月時出了本書，書中內容是在說他在那邊服務(或稱工作)六年，描述他的這一路走來的心路歷程，書名是＜邊境漂流＞，當中的有一段話我很喜歡，與大家共勉之：「正是因為這世界不夠美好，讓我們一同繼續努力吧!」

								
