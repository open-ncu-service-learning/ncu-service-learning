								 在二零零八年的8/8-8/23，我參加了由青輔會和中央大學課外組所合辦的''印度孟買志工服務學習營''。我們參加的是B團，成員主要是中央大學，輔仁大學，聖約翰大學還有高雄的夥伴。這個活動帶給我很大的感觸與啟發，以下是我詳細的心得。
