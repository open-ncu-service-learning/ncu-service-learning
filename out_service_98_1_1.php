<p>
<h3>
    <li>活動目的</li>
</h3>
<font size=small>

&nbsp;&nbsp;&nbsp;&nbsp;「2009全球天文年」的主題則訂為「我的宇宙，我來探索！」，它的目的在於傳達科學發現的欣喜，同時使一般大眾都能分享知識提升的喜悅。此次活動將特別強調科學教育、公眾參與，和年輕學子的投入。
為響應天文年活動目標，藉由體認夜空黑暗的可貴以及歷史性天文景點的重要性，加強對其文化及環境上的保存與保護。針對偏遠地學校，結合當地人文及天然環境特色，推出『打造星光小學，點亮星希望』的活動，深耕地方的天文科學教育。

</font>
</p>


<p>
<h3>
    <li>活動內容</li>
</h3>
<font size=small>
&nbsp;&nbsp;&nbsp;&nbsp;一般偏遠地區人口流失，學校裡的學生相對的減少，本活動將針對參加第一階段活動的單位，進行天文科學深耕的教育訓練。由中央大學偕同天文館工作團隊深入學校，針對教師與學童進行天文延伸教育，並整合當地天然優厚觀星環境，規劃天文觀測活動，發展其偏遠地區光害少的特性，將該校打造成一所星光小學，成為當地天文教育推廣的基地，透過教育資源的交流，增加偏遠地區小學教育價值，延伸其教育生命。
</font>
</p>

<p>
<h3>
    <li>活動心得</li>
</h3>
<font size=small>
&nbsp;&nbsp;&nbsp;&nbsp;本活動順利且成功的引領偏遠地區國小學童進入天文領域。我們安排的課程有太陽系介紹、星座盤操作、望遠鏡教學、認識月亮…..等，除了以上基本天文知識的教學之外，更帶領他們實際接觸天文專用的望遠鏡，親眼看到太陽的真面目讓他們留下驚嘆的深刻印象。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;為了實現課堂與實作結合的理念，特別在課程中安排學童DIY，透過DIY的使用教學，期望學童往後有能力了解天象。另外更利用無光害的優越環境下，進行夜間觀星，由我們帶領秋季星空導覽，秋季大三角、織女和牛郎、秋季銀河、飛馬座、仙后座等，盡呈現在眼前，讓頭頂上的星空不再迷糊不解，取而代之的是更有意義的星座解說。我們也把天文望遠鏡指向東方天空的木星及其衛星，讓學童透過望遠鏡觀賞木星及伽利略發現的4顆衛星，以清楚的影像配合課程的中講解，讓他們留下難以忘懷的記憶。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;
這次以啟發學童興趣為目的，推廣式的天文教育活動，不僅以生動活潑的方式呈現，也將原本艱澀難懂的知識化為清楚簡單的概念，讓學童們都能輕易吸收而不失其中真理。我們也獲得當地國小老師的認同與支持，因為當地教學資源缺乏，對於該校無光害環境加上抬頭即可仰望的星空，幾乎沒有天文方面知識的授與和啟發，而本活動正巧彌補了這個教育資源不足的缺憾。我們希望未來能繼續舉辦這樣深具意義的天文推廣教育活動，為偏遠地區服務與教學。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;
而在對本活動滿意度方面，從回收問卷的統計來分析，參加的年級分布大都平均分布，以托兒所的小朋友人數最多。而在課程內容方面，其中有68%的學童對於課程了解程度有良好的反應；而有76%的學童覺得本活動內容豐富。而在實務應用方面，有84%的學童覺得所學天文知識，在生活中有所幫助。而有72%的學童喜歡講師的教學方式；更讓我們欣喜的是，高達96%的學童認為本活動有助於其學習效果。而在整體活動上，有80%的學童對於整體活動的安排感到滿意；最後，以參加意願來看，有88%的學童表示願意再次參加相關天文活動。根據這些統計來看，這次活動相當成功，各項滿意度平均都達七、八成左右，顯示本次團隊所付出的努力，的確有豐厚的收穫。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;
最後，特別感謝行政院青年輔導委員會及桃園縣政府社會處補助本次活動，使得本活動得以順利地進行。對於偏遠山區學童有莫大的幫助，讓當地學童可以利用該校天然優厚的觀星環境，播下未來希望的種子。(文/課外活動組 黃裕隆)
</font>
