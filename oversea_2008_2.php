&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 最初參與的原因很單純，希望去大陸看看，想跟不同領域的人有更多互動交流，最後，因為認識外籍老師和烏干達合唱團的小朋友。因此，在培訓途中，得知因奧運活動的種種原因，張易中學夏令營將完全由志工策劃，卓時令我錯愕許久。雖是英文系的學生，但我的教學經驗幾乎是零，突然要面對一班五、六十位學生，實在無法想像情況會如何。我心中的確是恐懼與不安的，即使在培訓過程中，學到了許多教學方法，但我還是抓不太到方向，因此在強烈的恐慌中，我出發了。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
抵達張易中學已是夜晚，我永遠無法忘記那星空璀璨夜幕，及看到北斗星的興奮。當晚是難以入睡的，腦中想像著明天，即將面對的學生，心中既是害怕又激動，一切都是未知。這次的旅程，逼迫我面對許多自身的恐懼，快速適應環境、站上講台當老師、帶領活動集團康等，這些都是我在日常生活中，儘量去避免的情況。但在張易中學，我學會克服纏綁的心魔，了解到雖然我能帶給孩子有限，陪伴他們的時間很短，但在種種限制條件下，我們真心交匯，孩子們的學習熱忱，深深烙印在我心頭，認真大聲的唱英文歌，不顧一切的投入活動，給予我最大的迴響，回憶起來，依舊感動不已。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
但終究要分離，最後一天的相處，第一次如此深刻的依依不捨，看著孩子們一個一個的離開，眼淚無法控制的潰堤了，下次的相聚，遙遙無期。最後一個晚上，再次抬頭仰望，張易的夜空，我永遠無法遺忘。
