&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   「到寧夏固原去當英文夏令營的義工老師！」我從來就沒想過我會有當老師的一天，更何況是到寧夏固原這種聽都沒聽過的地方。神使鬼差的，我報名了，而且順利通過面試。在行前集訓時，看到其他義工們流利無比的英文讓我有點膽怯，畢竟我不是外文系的。但是大家相處的非常融洽，讓我有了一股「要一起完成這項任務」的感覺。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    搭機、轉機，再拉了近十一個小時的車程，終於來到了位於寧夏原州區張易鎮的張易中學。我們是在晚上九點多抵達的，天上的星星多的嚇人！連銀河都清晰可見。就在這絕美的星空下，開始了我們在張易的生活。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    在這純樸的地方，連夏令營的作息都非常規律。我每天早上七點準時起床之外，七點二十分準時蹲茅坑，因為連腸胃都很作息規律。茅坑是所有義工們一開始最大的挑戰，有些人抵死不進茅坑，不過最後都入進隨俗。大家的話題常是：「今天順利嗎？有沒有看到蛆蛆啊？」一邊蹲茅坑還可以一邊聊天呢！洗澡也是固定時間，全校只有一間浴室六個蓮蓬頭，除了分男女時段外，全部坦誠相見。一起洗澡、蹲茅坑、吃飯，讓我們二十位義工感情好到不行！<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    讓我最不習慣的應該就是吃飯，這裡的食物不是酸就是辣。偏偏我不吃醋也不吃辣，常常吃到眼眶泛淚。但是這是當地老師和廚師們特地為我們準備的，我還是心懷感激的含淚吞下。這裡的麵食非常手工道地，不管是饅頭、花捲、麵條、餃子皮，都是又香又有嚼勁，比起機器做出來的好吃太多了！<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    除了適應當地生活，來這裡最主要的目的是來當夏令營的英文老師。我的夥伴是清大外文系的高材生，所以教學方面沒什麼問題。我大部分都是在帶活動，雖然我天生大嗓門，不過一次要帶三十到六十位學生的活動還是常常喊到嗓子都啞了。從來沒有當過「老師」的我，在聽到學生喊「老師好！老師早！」的時候真的有嚇一跳。有點回到小學生時代的感覺，不過他們可都是國高中的大孩子呢！我帶的班級就是高中班，男生的個頭幾乎都比我和我的夥伴大很多。雖然他們看起來很成熟，可是不管是男生還是女生都是非常細膩而且純真善良的。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    幾天的相處下來，發現其實寧夏的孩子和台灣的小朋友其實沒有太大的差別。他們一樣愛玩遊戲，一樣不喜歡背單字。不一樣的是，他們沒有台灣小朋友的豐衣足食。放學後還要下田打工挣學費的學生比比皆是，有的學生還要到大城市去幹粗活。但是他們好學肯吃苦，常常走上三十幾里路來上學，讓我非常敬佩。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    天才表演的時候，我班上的學生大聲的唱出”Auld Lang Syne”這首代表友誼恆永久的歌，真的讓我很感動。離別時的歡送會上，班上女生輕輕的掉淚，讓我有點心疼。班上的男生大聲的唱出”水手”，做為大家的禮物。每個人寫的小指條上都說「不要忘記！要想念！」，但是我不希望他們如此傷心，我要他們快樂，這才是我來的目的。所以我笑著和他們合影留念。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
    現在回想起來，在張易的那幾天，就像是一場真是又太美好的夢！它留給我太多太美好的回憶！好慶幸自己有參加！謝謝寧夏的孩子們和老師們，因為有你們，我學到了很多。謝謝其他十九位義工，和所有的工作人員，因為有大家，這個暑假才這麼的與眾不同，這麼的美好！
