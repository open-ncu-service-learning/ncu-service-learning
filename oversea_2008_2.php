&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 我總覺得我們只是劃過寧夏的一道流星雨，我們在那片天空發光發熱，在這個暑假感動了那些孩子們，而流星劃過只是他們人生中的一瞬時間，縱然難忘，卻難再相見。　　透過這個夏令營，我所期望的是，我們能讓他們知道學習的快樂，即使家窮，志氣不能窮，身貧，讀書不能貧！　　<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

初來到這離固原市區幾十里路的張易中學，第一晚，我見到銀河躍然眼前與不小的三樓教學大樓，私以為這地方其實也滿美麗，能每晚都有星星作陪，心情就快活了起來。　<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　

在隔天便進行一整天的準備工作，雖然因為護照問題暫時少了一位好搭檔Janice，我們19位志工老師仍專注的與老師們一一討論教學與活動的細節，希望能有完美演出。　而擔心我們自台灣來，不習慣生活飲食，張易的老師及師傅們盡心的準備美味又道地的餐食給我們吃，也為我們加開了洗澡水加熱的時段。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　

感謝他們，在張易的食宿起居一切相當滿意且習慣。　<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

後來發現，許多我們認知理所當然的事情，來源都不易，熱水需有專門師父來開大鍋爐加熱，洗衣洗手的水需自己提水來用，諸如此類。　也才知道原來我們所用的教學大樓跟學生使用的課桌椅多是日本所捐建的，光政府的經費是不夠學校擁有好的教材跟師資的。　這地方，其實過得比想像中刻苦…<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

在與學生面對面聊過後，八成學生家裡是做農活的，加上兄姐在外地的打工，一年全家薪水不過三千人民幣。　我便好奇，繳完學雜費，學生們還有閒錢可使嗎？<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
看看他們服裝，數天未換，腳下一雙單色布鞋每天走十幾里路來上學，難能見到衣著有新花樣。　令我訝異的是，中餐時，見到孩子在福利部買一塊人民幣的膜膜來吃，光吃一半，是因為這大餅還含了他們的晚餐，一天餐食就如此打發了。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

同時也有許多人只買個方便麵就湊合作了一餐，而在飯堂門外排隊的，是一餐三元的土豆麵，加上個饅頭或是花捲，雖能吃飽，但沒有肉食與蔬菜，這些國家棟樑的孩子們，我擔心他們能否吃得營養均衡。　<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

我還是很愛這群孩子的，我看到他們沒有台灣學生的驕，只有不想輸的傲。從教學中，孩子們躍躍欲試的熱情，讓課堂更活潑，我也能感受到，其實他們很想學好英文，只是少了點樂趣，平時老師教的總是馬上就忘，囫圇吞棗難達學習之效。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　　　從活動中，激烈的競爭表示他們重視團隊與榮譽，也學會了合作才能成事的道理，強大的向心力就此凝聚了起來。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;　　　

最後一天的歌曲舞蹈表演是Emiliya的big big world，是我們兩個老師前一天下午才編出來的，排練不到一天，能有30人一起很流暢的動作，雖然唱不準，孩子們卻已舞出這首歌的要旨了，世界很大，愛是很遠的旅行，緣份讓我們聚在一起，而這一刻，我們即將離開，我們互相擁抱，感謝也感恩，從他們的微笑中，我知道雖然不捨，但是我們可以大膽說再見了，留下跨越海峽的思念也好，因為之後他們將成為真正的Big Big girls and boys!　　

流星之後，他們即將成長。
