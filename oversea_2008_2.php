&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   這次的活動是由台灣聯合大學系統主辦，溫世仁基金會以及千鄉萬才公司協辦的國際志工至中國大陸寧夏省張易舉行為期一週的英語教學課程。為什麼說是塞翁失馬呢？因為當初這個計畫在籌備的時候，原本是要由一群來自英美的大學生領軍與台聯大四校的學生當助教負責教學中翻譯的工作。其實我們一開始都對這群無緣相會的外國大學生充滿期待，知道他們因為簽證問題無法前來後當下的反應除了失望其實充滿著錯愕，因為少了他們，所有的教學以及活動責任頓時都在我們二十位同學身上了。距離動身前往寧夏只剩不到一個月，而我們20位同學，第一次見面….。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

  在期末考結束後，中央大學課外組馬上安排了四校的同學六月三十號開始進行為期一週住校的集訓。感謝台灣聯合大學系統副校長、顧問以及中央大學課外組的安排，集訓課程非常的充實，說實在的，也非常的累人。在一週內，二十位同學要從陌生人到合作夥伴並且一起組織安排一個為期一週從早到晚全天的活動，參與學生人數更高達將近400人。在集訓的課程中，學校請到伊甸基金會，陽明大學教授，以及中央大學的兩位英語教師來給我們上課。為什麼說是千頭萬緒？因為我們面對的是一望無際的未知之旅。當地學生的英文能力？學校的設備？有沒有操場？有沒有電腦？該準備什麼？該上什麼課？幾點開始上課？全部幾乎都是「不知道」。集訓在這期中發揮了很大的效用，因為我們什麼都學，從英語教唱，課程安排，文化差異可能的衝擊和影響，到該有態度想法，一週連續的活動，不僅讓我們都繃緊了神經準備，也無形中，拉近了彼此的友誼和距離…。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

  這整個計畫，要是沒有四校聯合的同學，我肯定一定會遜色許多。我們有來自醫學系的同學，資工所的朋友，人社系的夥伴。誰說一個英語教學的活動就是一定要英文系的學生？縱使我是英文系的一員，但我們20人非語言科系的同學還佔了超過一半呢！大家所擁有的經驗不同，思維不同，處理事情的態度和想法也都有所相異。在這期間，我們互相激盪，開會討論，溝通學習；有時後難免出現摩擦，我們曾經為了下午的活動到底要採各班自己進行或是全校集體活動而爭辯不休，還一度有選邊站的火藥味，但是我很高興我們一切的討論爭辯最終都有得到一個理性和最佳的結果。大家各司其職，努力發揮自己的長才，清大資工所的Georg為了大家寫了一個網上的討論區和架了一個FTP站分享資訊和照片、中央光電系的治樺也運用自己的活動經驗把營期的活動詳盡且妥善的安排規劃，也給了大家不少的經驗和學習、陽明醫科的同學我不得不承認他們真的好聰明，規劃計畫想的比誰都快都好。從夥伴身上學到的東西，大家在不同學校不同科系所學到的東西互相分享、體會也是讓整個計畫順利進行的一大因素…<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

  現在我仍然記得當初伊甸基金會主任說的一句話：「你們這個團隊，在兩週後會知道是分裂還是凝聚。你們現在都笑笑的，因為你們不認識對方，兩週後就會知道你們是成功還是失敗了。」當時主任的這個提醒，現在想起來，真的是一個寶貴的經驗談。也的確，大家的意見漸漸開始分歧和爭辯是從集訓的尾聲才進場。四校的同學一方面要顧及活動的順利，一方面要考量自己在短時間內所能承擔和完成的責任，大家各有不同的角度去思考討論我們的計畫。在那個時候，個人是否要採取比較強勢的態度？或是要如何去溝通妥協？我當時身為隊長，要如何去從中互相協調變得極為艱辛，無可厚非我也會出現我自己的立場，要怎麼讓大家好好坐下來討論變成一個很重要的課題。不過我很慶幸，因為在較激烈的爭辯後，大家往往能自動回到活動的初衷，理性地去思考根本上的問題，我這個隊長，說實在的就也就是個和事老，牆頭草兩邊倒，面對這邊說兩句話，面對那邊補個微笑，最後還是尋求最民主的方式投票表決。在兩週後，我很高興地發現，藉由大家理性的溝通和組織，我們不是一盤散沙互相嫌棄，而是成為了一個互相扶持，有著強大向心力的「台灣聯合大學國際志工」。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

    到了寧夏省固原市，一開始受到當地教育局和台辦的熱烈歡迎，同學們都想著我們可能是先甘後苦，但是我現在要說，結果我們一路上的收穫都是甘之如飴的。看著小朋友從羞怯不敢舉手發言，到會主動跟我們聊天問問題，心理的那份成就和感動實在是無以言喻。我們藉由導師課面對面聆聽他們每個人的故事，讓我們知道他們的背景，他們的困境，也讓我們能更體會在那樣辛苦的環境下，這是一群多麼努力的孩子。我常說的，我看他們是真的在「求學」，他們是真的用求的，有時候，甚至還會求不到！一大包的饅頭就是三天的糧食，那樣的日子，不親身體會是不會懂得。營期只有短短八天，當初大家就已經了解我們做的就是「陪伴」二字，但是到了離別，還是在想能不能多待一些時間？最後離別時大家都哭成一團，我也慚愧自己的能力真的不足夠給他們更豐富的學習經驗。雖然也就只有至短短七八天的日子，我所能體悟到的卻是在台灣相較高度發展的社會下所感受不到的東西。那群孩子的知足、刻苦，我看到了，也期許自己努力像他們學習。                     <br />
							
