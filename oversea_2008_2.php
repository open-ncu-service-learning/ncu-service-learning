&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 回憶過去一個多月的情景，淚水與歡笑交織成感動，腦海浮出一幅幅美麗畫面。第一次當國際志工，前往大陸寧夏進行服務，一切對我來說，都是如此新鮮。由於這次營隊的主題是英語教學，對於非英語系的我來說，實在是一個挑戰。行前訓練的那四天，我們認識了來自其他三所大學的同學，在兩位十分專業的英語老師的協助下，我們學會了如何進行課程的規劃與教學，本來對自己英語能力的擔憂同時也減輕不少。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
出發前，我們總是擔心著自己是否能適應那邊的環境、氣候，擔心著那邊學生的程度是否連二十六個字母都不太熟悉，擔心著活動規劃的流暢與否。直到抵達寧夏，見到校長與學校老師們熱情地迎接我們，讓我卸下心中懸掛已久的大石。在寧夏，我們看到了廣闊的黃土以及雄偉的山脈，新鮮的空氣、樹葉上清徹的露珠，好久沒聽見的鳥鳴，平常生活在都市的我們，彷彿走進世外桃源，鬆下緊繃的神經，愜意地走在校園裡參觀。晚上，滿天的星斗、閃爍的銀河與不時造訪的流星，更是讓我們開了眼界。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
第一個上課日，我在進教室前一秒鐘，用力地深呼吸，帶著滿臉的笑容進入教室。班上的學生原先都緊繃一張嚴肅的臉，在我站上講台的同時，大家漸漸露出雪白的牙齒，展開了笑容。我想，也許是昨晚的禱告成真了吧！班上有一半以上的學生來自張易中學，其餘則是來自平地或是其他地區的會員學校，由於張易的學生是從初中一年級才開始學習英語，平地學校的學生則是從小學三年級就開始接觸英語，兩者之間存在著程度上的落差。但是學生知兼併沒有因為這樣產生嫌隙，來自平地的學生會主動協助我們的教學，並熱心地成為英文小老師，幫助張易本地的學生，他們的付出讓我十分佩服。
光陰似箭，歲月如梭，第一天全體的兔子舞，本來因為學生個性保守害羞，不敢與異性牽手而無法順利進行，在相處一些日子之後，學生們之間也變得熟稔，接著登場的闖關遊戲，漸漸熔化了那層看不見的羞澀與陌生。看著這幾天下來他們的成長與改變，從膽怯到自信，從畏縮到展現熱情，不敢發問到踴躍發言，讓第一次進行英語教學的我，感到十分欣慰與感動。導師時間是一個讓我印象深刻的部分。我們和學生一對一的談話，瞭解他們生活背景與學習狀況，並給予建議、協助與鼓勵，期盼能讓學生在學習英語的路途上走得更加自信與順利。身為老師的我們，由於年紀尚輕，與學生亦師亦友，幾天下來也與學生建立起深厚的友誼，離別的那天，淚水淌在心頭、倘在臉上，不捨放開這一份牽絆。<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
回到台灣的那天，再次踏上這片熟悉的故土，心中千頭萬緒，思念的湧泉讓我們久久不能自已，複雜而沈重的情緒降低了返國的喜悅。如果說夏令營期間我們在他們心中已經埋下小小的幼苗、播下種子，此刻，我殷切期盼著，未來看見他們能長成一棵棵高大的樹木，有所成就，對社會能有一番貢獻，進而將種子傳到更多孩子的心中。
