<div>
由清大、交大、陽明和中央四校共組的台灣聯合大學系統，為增進學生交流與國際視野，今年首次與溫世仁基金會千鄉萬才計畫合作，四校廿名國際志工將前往中國寧夏銀川的張易中學服務，預計七月廿三日啟程，為期十天，進行大陸中學生英語夏令營活動，服務弱勢偏遠學區的孩童。

