<h3>
   <li> 活動內容</li>
</h3>
   
   <font size=small> 
<ul>
<li>
始業式：
宣告營隊開始的始業式內容有唱基服之歌、輔員的介紹與校方致詞，校長或主任都有幫我們向小朋友們宣導要聽大哥哥大姐姐的話，並且叮嚀不要到處亂跑，而主任或校長的叮嚀都非常有用，小朋友們都頗聽主任或校長的話，也很謝謝幫我們奠定接下來這幾天的小朋友秩序。
</li>
<li>
歡迎會：
表演舞蹈以及團康遊戲與帶動跳還有互動式的戲劇，讓小朋友對輔員能夠卸下陌生感，留下良好的第一印象還有拉近彼此的距離，而小朋友也從剛開始彼此不熟和冷漠中解凍，隨著歡迎會的進行，小朋友的熱情也漸漸加溫，逐漸與還不太熟的大哥哥大姐姐及其他小隊隊員產生了認識。
</li>
<li>
生活創意DIY：
利用一些小物品，讓小朋友自己動手做好玩又有趣的小玩具，以實作倡導環境保護，並讓小玩具當作小朋友營期結束後的紀念品，小朋友一步一步的自己動手，漸漸的也越來越專注於自己的成品上面，當最後成品完成時更是給予小朋友滿滿的自信及對自己作品的驕傲，小朋友都把做好的作品當作是自己的炫耀。
</li>
<li>
帶狀：
每天午飯結束之後都會有一段時間讓小朋友進行靜態且不動腦的課程，緩和一下心情，消化剛用畢的食物，每天的活動都不一樣，從第一天的小朋友動手做自己的小名牌到之後玩團康、教唱營歌及教小朋友打一些簡單手語，提供小朋友輕鬆的時間，讓他們在飯後休息一下。
</li>
<li>
團隊大地：
在校園各地設計各式各樣的遊戲，讓小朋友活潑的心情完全奔放，並在團隊遊戲中，讓小朋友們學習團結合作的重要，而小朋友也在一次次的跑關當中，與隊友之間也越來越熟悉，增近了彼此的感情，不過在跑關中也有一些因為意見不合產生的小小摩擦，不過最後都能彼此體諒完善收場。
</li>
<li>
晨間活動：
在美好的一天剛開始時，來接受輔員的熱情，ㄧ同帶動跳，或是團康加上有關環境保育知識問答等小遊戲，提振精神，小朋友從早上剛到學校時還處於沒有這麼有精神狀態下，經過有趣的晨間活動後大家的精神及熱情都達到了最高點，活力滿滿的準備展開一整天的課程。
</li>
<li>
美食DIY：
透過自己親手製作食物的過程，啟發孩子的創意，以及培養孩子獨自思考發揮創作的能力，並享用自己親手做的食物，小朋友拿著自己動手做出來的美食創意作品到處向大哥哥大姐姐介紹，經由大哥哥大姐姐的鼓勵下，小朋友們勇於用自己的創意作出屬於自己的作品，每個都充滿了個人特色及個人創意，而食物也讓他們吃得很開心。
</li>
<li>
觀念：
我們營隊的重點部份，課程裡會提倡環境保育，並且會以遊戲與講解並進的寓教於樂的方式，希望小朋友了解環境保育的重要，小朋友經由一次次闖關中，得知了許多有館於環境保育的知識，也學會了如何正確的把垃圾分類，在關主的引導之下，小朋友們都能做出正確的判斷及得知正確的知識。
</li>
<li>
運動會：
設計幾種運動的關卡，讓小朋友可以在戶外進行些運動，學習運動家精神，一開始在大哥哥大姐姐帶領下進行暖身，並且一起宣誓運動員宣誓，將運動家精神強調，然後開始出去進行各式各樣的運動及比賽，小朋友們都在操場及跑道上到處開心的奔跑，也讓小朋友滿滿的精力獲得發洩的地方。
</li>
<li>
古早廟會：
在校區內擺設許多的小遊戲，讓小朋友自由跑關，體驗廟會的趣味，給予小朋友自信與成就感，
小朋友到處去尋找散布在校園裡的各關關主，並向他們比賽，贏的可以得到積分獎賞，不過輸的小朋友我們也是會給予比較少的獎賞鼓勵他們繼續的跑關。
</li>
<li>
彩繪大地：
利用校園內大自然的產物，像是樹葉、花朵、小石頭等等，讓小朋友發揮想像力，做出獨特的創意畫作，小朋友們在畫紙上創造出屬於獨一無二的畫作，讓他們了解到大自然裡面充滿了許許多多的驚喜，並且最後讓各位小朋友們向大家介紹自己的作品，讓他們學著如何向別人表達自己的東西。
</li>
<li>
晚會：
在營期第三天的晚上我們準備的晚會，希望讓小朋友在學習之餘還能看到我們準備的表演，讓小朋友能永遠記住這天晚上以及營期的快樂回憶，而小朋友在大哥哥大姐姐精彩的表演中一次次的掌聲，希望讓他們看到我們的用心，並且讓他們感覺到晚會是專門為了他們準備的，希望可以讓小朋友有被重視的感覺。
</li>
<li>
歡送會
在最後一天營期要結束前，輔員透過躲貓貓的形式，讓小朋友們找出所有輔員，並留下簽名與合照，作為一個完美的結局，歡送這個營隊的結束，小朋友紛紛拿出自己的筆記本要我們大哥哥大姐姐留下簽名，好讓他們可以珍藏當作回憶，而小朋友與大哥哥大姐姐們之間都是依依不捨的。
</li>
<li>
綠化大地：
輔員與小朋友一同清掃國小週邊環境，讓小朋友養成環境整潔的習慣並且培養高度的責任感，在分配之下，小朋友們盡力的打掃自己分配的環境，收拾這幾天來為學校製造出的垃圾，讓他們了解到自己對於還境是有責任的。
</li>
<li>
結業式：
最後一天的結業式則有輔員感言與全體學員輔員大合照，我們大哥哥大姐姐說了很多祝福的話，也要小朋友們過的平平安安，開學要聽老師的話不能調皮，而最後的大合照更是替整個活動留下了完美的結局。
</li>
</ul>
   </font>
