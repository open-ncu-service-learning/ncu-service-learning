						第 一 條	依據教育部「大專校院服務學習方案」為提升良好公民素養及實踐公民責任，有效培養學生未來就業核心能力，暨本校為養成學生誠樸、負責、自律、勤勞、服務與 互助合作之美德，特訂定學生服務學習施行辦法（以下簡稱本辦法）。
						<br /><br />
						第 二 條	本校服務學習列為畢業審查條件，區分為課程與非課程之相關活動學
									習二部份。課程部份為本校大學部學生，必須修習之服務學習課程，
									共計二學期，每星期二小時。
									非課程之相關活動學習部份，為本校大學部學生參與各項非具正式學
									分課程與活動之學習，並以學習護照呈現之。
									轉學生倘於他校曾修習（及格）相關服務學習者，得申請抵（減）免。
									領有身心障礙手冊或特殊個案學生，經導師、系主任同意，送學務處
									服務學習辦公室審查通過者，得免（減）修。
						<br /><br />
						第 三 條	為貫徹服務學習，成立「服務學習指導委員會」（以下簡稱委員會），
									由副校長擔任主任委員，委員包括：教務長、學務長、總務長、總教學
									中心中心主任、通識中心主任、藝文中心主任、各學院院長、學生代
									表（名額不得低於五分之ㄧ），負責服務學習策略指導與工作規劃。
									為執行服務學習工作，委員會下設「服務學習執行小組」（以下簡稱
									執行小組），由學務長擔任召集人，成員包括：學務處服務學習辦公
									室主任、教務處課務組長、學務處生輔組長、課外組長、總務處事務
									組長、各學院一名系主任、服務學習導師、學生會代表二名，負責服
									務學習之執行與督導工作。
									服務學習辦公室成員與服務學習計畫相關行政人員於委員會召開時應
									列席。
						<br /><br />
						第 四 條	服務學習，得涵蓋校內、外單位，並在學生安全學習之考量下設計、規劃與辦理。
						<br /><br />
						第 五 條	學務處依本辦法規定，擬訂學生服務學習實施細則，經本辦法第三條之委員會會議通過後，送校務會議備查施行。
						<br /><br />
						第 六 條	服務學習課程由各系教師擔任指導老師，開設大學部課程。轉學生與重修生由學務處安排指導老師，開設學期課程，為無給工作。
						<br /><br />
						第 七 條	服務學習課程內容，涵蓋校區環境與館舍清潔、典禮集會行政服務、社區服務、專題服務講座等。
									非課程服務學習，含括四大領域：服務學習、生活知能學習、人文藝術學習、國際視野學習。
						<br /><br />
						第 八 條	服務學習課程為必修，除各系另有規定外，以零學分為原則，需全部通過，始得畢業；未通過者，必須重修。若為特殊課程設計者，需經相關課程委員會及教務會議審議通過，並可有學分。
									本課程成績，由指導老師依學生實際表現，給予評定及格或不及格。
									本課程除公假外均需到課，未到課者須行補課；缺曠課達一定時數者，該學期成績不得評列及格。
						<br /><br />			
						第 九 條	學生學習護照應涵蓋學生在校期間，相關學習紀錄，並訂定基本學習時數，以為本校對培育之畢業學生，具有實踐公民責任的能力。
						<br /><br />
						第 十 條	學生服務學習，應考量學生學習成效，給予適當之獎勵，並訂定於學生服務學習實施細則中。
									對本辦法執行著有績效之單位主管、指導老師及行政支援人員，由執行小組簽案送學校予以獎勵。
						<br /><br />
						第十一條	執行本案經費由學務處編列，於執行小組會議報告。
						<br /><br />
						第十二條	本辦法經校務會議審議通過後施行，修正時亦同。
