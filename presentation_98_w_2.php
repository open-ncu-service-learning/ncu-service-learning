<li>旅行之美-一起來慶祝（課程）：藉由課程讓小朋友們對於世界各國的節慶能有更多的認識。</li>
<li>旅程的回憶（課程）：藉由課程讓小朋友們發揮創意製作獨一無二的相框。</li>
<li>探索之旅-世界大驚奇（課程）：藉課程讓小朋友們探索世界各地的特別之處。</li>
<li>環遊世界（大地）：藉由課程凝聚各小隊的感情，並培養小朋友們合作解決問題的能力。</li>
<li>可愛卡通廚房 (DIY教學)：開場戲、做菜教學、動手做菜。</li>
<li>世界之旅（大地）：藉由課程凝聚各小隊的感情，並培養小朋友們合作解決問題的能力。</li>

