的地方可以解決，但是如果要大號，非要等到真的忍不住了，才衝到
廁所去。記得前幾天，有可能因為太害怕了，害得我有好幾天都沒有
排泄了，在家裡的我可是每一天都定時排泄喔。可能是因為看到垃圾
場的蒼蠅滿天飛，而且在下大雨後又看到蛆在路上爬行，真的是害怕
到都上不出來了。但是好像在第三天還是在第四天吃完早餐後，肚子
覺得超級脹的，很不舒服，抓了幾張衛生祇(對了一定要記得帶濕紙
巾，味道會淡一點)鼓起勇氣衝到了廁所。在去的途中還遇到了 Belle，
打了聲招呼就一起衝到廁所了。到了廁所前的台階，一定要記得捲起
褲管。進去廁所後，往坑裡面一看，我真的被下傻了，每一個坑裡面
都有滿滿的 poo-poo，有可能之前已經先做足了心理準備，味道沒有
想像的那麼濃，但是眼前的景象真是不能接受。比較好的是，我們是上教師的廁所，每一個坑之間都還是有隔間的而且並沒有想像中有很
