大挑戰。但我很衰，第一天就拉肚子所以被逼得一定要去茅坑，一定
要習慣，差不多兩三天後，我就克服了，口罩也不用帶了。洗澡的地
方，每天他們那裡的教職員工都會很好心的幫我們燒熱水，但那個水
總是不是太燙就是太熱，但洗起來還是蠻舒服的，畢竟跟學生比起來
我們有水可以洗澡就已經要感恩了。那裡的食物實在很好吃，都他們
自己做的，麵啊饅頭什麼的，都純手工很有味道，很對我的胃，飯後還有超甜的西瓜跟哈密瓜。那裡的氣候很乾燥，建議一定要帶護唇膏
跟防曬及保濕的。厚外套也一定要，因為那裡溫差很大。 <br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 幾天下來的心得收穫不少，而我也認真的思考了一些問題。像
是，我是去服務自己還是別人？我給的是我想給的，還是他們需要的？前隊長說，去的時候不要覺得你們去教他們英文是去幫助他們，
短短幾天夏令營英文能力也不可能提高到哪裡去，重要的是陪伴，你
是去當他們的大哥哥大姐姐的。於是當我以平等的方式去跟他們交流
時，我發現被豐富的是我的生命。一些他們有的，可能是我們已經失
去的。 
