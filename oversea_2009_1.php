為有學校大廚、老師們以及老師們的老婆們的幫忙，讓我們都不用煩
惱吃的問題，反而每天都很期待吃飯的時候。每天早上都有各式各樣
的「饃饃」(其實就是指包子、饅頭這類的)，饃饃包括了花捲、菜包、
肉包，真的相當好吃呢。最後幾天還有有趣的炸饃饃(大廚一定是聽
到王董的心聲了)。還有小米粥或是糯米粥，加入少許的糖真的好好
吃。有涼拌的冷盤，有時會是紅蘿蔔加上白蘿蔔絲，有時候有海帶絲
加上胡蘿蔔絲，有時候又是土豆(馬鈴薯)絲加上胡蘿蔔絲。除了這些
外，每天必備的還有水煮蛋。中午大部分都吃「飯」，有時候會有一
鍋很香又很濃的雞肉或是牛肉、羊肉鍋可以來拌飯，當然也少不了一 些小菜摟(像是番茄加糖)，再加上海帶或是番茄清湯，最後幾天還會
配上西瓜或是哈密瓜當飯後水果。晚上的主食通常都是「麵」，麵的
嚼勁、彈性都好的不得了，有時候光吃這個麵就可以滿足了，晚餐當
然少不了小菜、湯、水果。每天都吃的飽飽的，回家量了體重，果然
增加了幾公斤。 <br />
