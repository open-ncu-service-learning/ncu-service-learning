</li>
<li>
結業式
：這次營隊最後的一項活動，工作人員發下卡片讓小朋友互相寫下心得，小朋友寫給工作人員的童言童語讓許多工作人員感動在心，這次的營期也在快樂溫馨的結業式畫下句點。
</li>
