<li>
大型團康 II
：第二個大型活動把氣氛帶到最高點，小朋友將在遊戲中獲得上台表演的機會，也讓小朋友有愉快的一天。
</li>
<li>
早活動
：一大早的早活動主要讓小朋友動起來激發精神，經過第一天營期小朋友彼此認識之後也讓此活動進行非常順利。
</li>
<li>
找碴劇
：找碴據一向皆為小朋友的最愛，除了可訓練小朋友的觀察力，也藉由這個活動更融入此營隊，也激發他們對於課程的興趣。
</li>
<li>
課程─天籟特訓班
：合唱概念的介紹。使小朋友從生活中就可知道合唱無所不再，隨時都可以練習。此課程因為較為接近流行音樂小朋友接受度高，小朋友最後也唱得很開心。
</li>
<li>
課程─音樂城堡
：說明簡單的音樂知識、一些現代基本的曲風，再以MV導出音樂，讓小朋友認識影片與音樂間的密切關係。且延續上一堂課的氣氛，此堂課小朋友吸收相當不錯。
</li>
<li>
大地遊戲
