課程─認識樂器
：藉由投影片教學以貼近小朋友們的方式介紹圖片上的樂器及其聲音，增進小朋友對樂器的了解。並播放影片加深小朋友對各種樂器的認識。
</li>
<li>
課程─課程大地
：以不同的形式，將課程上學到的樂器、歌曲、音樂等，在遊戲中使小朋友享受音樂的樂趣。
</li>
<li>
課程─影片欣賞
：除了常見的樂器外，將透過影片介紹打擊樂的不同風貌，讓小朋友知道音樂可以不侷限在於一般樂器上，它是生活處處皆樂器的道理。
