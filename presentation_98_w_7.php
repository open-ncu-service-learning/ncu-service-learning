活動─始業式
：為兩天營期的第一個活動。藉由活潑的戲劇演出與教學，引導我們與小朋友互相認識後，讓小朋友更加認識接下來兩天，將帶著他們參與活動的大哥哥大姊姊們。並且藉由營歌教唱，使小朋友們動動口、動動身體，有活力地揭開此次音樂營。
