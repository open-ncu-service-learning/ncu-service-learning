<h3>
   <li> 活動目的</li>
</h3>
<ul>
<li>建立學生社會服務的觀念，並能保持隨時為維護環境的奉獻理念。</li>
 <li>引領學生對大自然有更深的認識，感受自然氣息的洗禮，增加大自然的體驗，以導覽解說方式加深對環境資源瞭解，藉以走出校園親近大自然，體驗三峽生態環境之美。</li>
 <li>同時藉由社區深度文化之行，結合歷史、人文、休閒生態等多元特色，強調文化紮根，落實社區營造，運用地方文化特色與文化節慶活動，促進學生參與認識社區多元發展面向，以凝聚鄉土情感、建立社區認同。</li>
<li>響應世界地球日及本校所推動之「綠色啟動（Green Initiative）」計畫，強調綠色經濟和環境永續，愛護我們居住的環境和地球。</li>
</ul>
