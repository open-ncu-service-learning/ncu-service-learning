    當隊長後體驗到了很多前所未有的感受，在當隊長的過程中，也很幸運的遇到了很多貴人，無論是國小的校長跟主任都好客的讓我受寵若驚，也都多虧了他們的幫忙，讓這次的營期都很順利，九芎國小跟林內國小的小朋友們，也是我這次出隊裡得到的寶藏，我也覺得很幸運可以服務到他們，而出隊中得到的回憶，在營期結束後也不停的讓我回想起來。
    「基層文化服務社」本著「基於我心、服於我愛」的精神，在每年寒暑假去全台各地，每個人仍然是本著服務的精神，去享受這從出隊準備一直到營期結束的所有過程，不管出了多少次隊，每一次的出隊都是獨一無二的回憶，也要謝謝很多人，謝謝你們。
                                                                   

