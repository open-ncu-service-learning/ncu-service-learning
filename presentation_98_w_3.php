<h3>
   <li> 活動內容</li>
</h3>
   
   <font size=small>
<ul>
<li>
始業式與結業式：在開始與結束時的始業式與結業式。始業式會有輔員的介紹與
　　　　　　　　校長致詞，最後一天的結業式則有輔員感言與全體學員輔員大
　　　　合照。
</li>
<li>
歡迎會：利用表演舞蹈以及團康遊戲與帶動跳還有互動式的戲劇，讓小朋友對輔員不感到陌生，提升小朋友們對這個營隊的興趣。
</li>
<li>
帶狀課程：在每天午飯結束之後的時間讓小朋友進行輕鬆的課程，聽一聽各國的民謠，也讓小朋友們對下午的課程提高興致。
</li>
<li>
大地遊戲：在校園各地設計各式各樣的遊戲，讓小朋友抒發活動力，並在團隊遊戲中讓小朋友們學習到團結合作的重要。
</li>
</li>
晨間活動：一天的第一個課程，用簡單的體操來讓小朋友們擺脫睡意，打起精神來面對接下來的活動。
</li>
<li>
主題課程：我們營隊的重點部份，運用一些小遊戲來讓小朋友們了解各個國家的文化，提升小朋友們的國際觀，也會讓他們學習到一些新的知識。
</li>
<li>
廟會：使用類似日本廟會的方式，在校園的各個地點擺設小遊戲與關卡，體驗廟會的趣味，給予小朋友自信與成就感。
</li>
<li>
惜別晚會：在營期第三天的晚上我們準備了團康式晚會，將這次營隊的氣氛帶到最高潮4，讓小朋友能永遠記住這天晚上以及營期的快樂回憶。
</li>
<li>
美食DIY：透過親手製作各國特色食物的過程，讓小朋友們更了解各國文化，以及培養孩子獨自思考發揮創作的能力，並享用自己親手做的食物。
</li>
<li>
創意DIY：利用一些回收物品，讓小朋友自己動手做各個國家的傳統藝術品，以實作倡導環境保護，並讓小玩具當作小朋友營期結束後的紀念品。
</li>
<li>
運 動 會：舉辦一個小型的國際奧林匹克運動會，讓小朋友可以在戶外進行些運動，學習運動家精神。
</li>
<li>
假案偵查：設計一個簡單的劇情，讓小朋友們去闖關調查劇情裡的關鍵，並運用邏輯思考的方式，推理出最後的答案。
</li>
<li>
歡 送 會：在最後一天營期要結束前，輔員透過躲貓貓的形式，讓小朋友們找出所有輔員，並留下簽名與合照，作為一個完美的結局。
</li>
