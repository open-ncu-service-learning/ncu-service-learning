      <div class="TabbedPanelsContent">在寒服營隊期間，有許多感動和盼望。
    每天的生活，從五點起就待在廚房工作，直到夜晚向執行長上呈意見提供參考，白天晚上在大夥忙碌或聊天時，我趁隙休息補充體力，好在時間點平安接送大家，做好司機的本分。
    雖然沒有直接地與大夥互動、參與各樣服務，我說不出服務生活中的點滴，但我知道我們是個團隊，彼此互相為肢體，都是營隊裡不可或缺的一部份。
    我感覺得到我們這個大群體的成長茁壯，看得見我們整體的改變和更新。看到我們中醫社大夥在營隊裡活出的生命，我就見到中醫復興的盼望。
    從問卷、做菜、義診與各樣活動當中，我見到我們中醫社每一個人都在成長。從怯懼拜訪陌生人家，變成勇於探訪關懷每一個人；從與各樣人士疏於應對談話，變成與所有人對話皆能應答如流得體；從不熟悉按摩刮痧技巧的初學者，變成得心應手隨時可承接患者的專業人士；從準備不足的伙食，變成豐盛有餘的三餐；從不熟絡的社團學長姊學弟妹，變成有革命情感共同回憶的好朋友；從平時社課學到知識而獨善其身，變成在義診盡心服務之兼善天下；從凡事要請教人的學生，變成可獨當一面並持續精進成長的學者。
    經過五人、十人的訓練，我們能承接七十三人的服務，每個人都是義診精兵。大家一起教導民眾正確的健康知識，分享中醫照顧身體的方法，我不知道這次營隊在台中灑下的種子何時會發芽，但我看到中醫社在一學期的社課之後，每個人都結出各自甜美的果實。
    這次營隊真的讓我從服務當中，學習到人生以服務為目的，越服務越獲得成長。

