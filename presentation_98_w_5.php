      <div class="TabbedPanelsContent">在寒服之前，說實在，身為一個副執行長我不免有些擔心，主體是一群還沒訓練好的大一生，突然又得知醫生不能到的消息，再加上想要請求贊助的藥廠遲遲不回應我們的聯絡，對我的打擊可是相當大，雖然有學長說這都是可以預期的但，在活動前不免抱著忐忑不安的心。

在這個團體之中，有不太熟的大一學弟妹，有要畢業的學長，一時間，我還真不知道要怎麼在活動中去管理整個團體，幸好，學弟妹們都很支持這個活動，搶著一馬當先的去服務，而學長更是不耍任何大牌的無私付出，不僅奉獻自己的車子，每每晚睡早起，大家從不熟識到相互玩笑，看的這種情況心中只有感到無限的欣慰。

雖然是如此，但當地居民剛開始對我們似忽有所戒心，剛開始的活動人少的有點可憐，不過在接連下來的幾天，他們也明白了中醫的好，並呼同引伴的來參與我們的活動，從閒到發荒的五個人十個人到手忙腳亂的七八十人，這些都是很特殊的經驗，將是我不敢說我們對於中醫的專業知識有每個人全盤的瞭解，但我們對於這整個活動所付出對於當地的關懷一定不輸給在當地的任何一位有職照的醫生。

學習服務，服務學習，兩者層層相關，我希望下一屆的領導者，依然能帶給往後的學弟妹們這一種感動。


