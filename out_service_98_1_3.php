&nbsp;&nbsp;&nbsp;&nbsp;「本計畫之解說員與講師以校內學生為主，教職員為輔，活動解說與正規教育課程相輔相成。除能培養學生規畫、主持活動之能力，更可為參與活動之新移民子弟帶來趣味橫生又豐富的教育資源，提供多元學習的方式與場所。希望能藉此科學體驗活動，讓學生享受到科學學習的樂趣，進而喜歡科學、愛上科學。

