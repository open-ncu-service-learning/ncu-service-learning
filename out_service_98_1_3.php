<p>
<h3>
    <li>活動緣起</li>
</h3>
<font size=small>

&nbsp;&nbsp;&nbsp;&nbsp;科學教育中心草創之初，基於人力、物力資源欠缺，現階段搭配物理系演室實驗室之演示實驗品，結合中心近140平方米之展示廳為場地，積極推廣科教活動，至今已經服務過百餘個團體參觀。自97學年度開始，由科普活動的接觸中體認到，新移民子弟的確亟需在正確的科學學習與認知上接受指引。年輕的學子們也求知若渴，各級學校的校長與老師們更是熱心地想讓他們的學生有更好的學習環境。而物理演示教學可以有效地引起學生的好奇心和學習動機，藉由演示過程，正確的傳達科學概念和方法，讓觀者在批判與思考時，有正確的指引。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;另一方面，中央大學是國內天文研究起步最早的大學，而校內科一館所設置的24吋天文望遠鏡，在以往二十年間都是天文學界的代表性觀測工具，對天文研究及人才培育做出重大貢獻。由於中壢地區的觀測條件已大不如昔，且鹿林天文台也逐漸取代其研究功能的情況下，本計畫擬將24吋天文望遠鏡轉為科學教育與天文普及教育設施，輔以小型儀器的太陽觀測，提供各級教育團體教學觀摩、使用，同時也擬發展網路天文教學網頁與新聞報導等，以期肩負科學教育任務，同時也記錄中大的天文發展歷史與成果。<br/>
&nbsp;&nbsp;&nbsp;&nbsp;一年來科學教育中心推廣科學教育活動有成，無論是各級學校前來參訪與觀摩科學演示，抑或是假日的親子科學體驗，均吸引了數千人次的愛好科學民眾。除了善盡大學的社會服務之責，更增進本校之知名度，與在科普教育積極投入的形象，對學校的招生宣傳等工作，有絕對正面的影響。

</font>
</p>


