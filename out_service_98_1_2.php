<h3>
    <li>活動目的</li>
</h3>
<font size=small>

&nbsp;&nbsp;&nbsp;&nbsp;「節能減碳是目前全球注目的焦點，低碳及無碳能源的研究與應用，更是能源產業的趨勢與方向。
身處新世紀的我們，除了了解現階段能源的開發與應用之外，更應對所居處的生活環境有相當的了解，才能真正擔起環境關懷的重責大任。本校在當前國家的環境議題、能源議題等等，皆有相當重要的權威與重要性，對於環境關懷的科普教育推廣，更有其教育責任與社會責任。


</font>
