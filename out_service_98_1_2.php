<p>
<h3>
    <li>活動內容</li>
</h3>
<font size=small>
&nbsp;&nbsp;&nbsp;&nbsp;本次活動，將從大桃園地區的濱海地帶作為活動範圍，認識地方的都市發展歷史和重大汙染事件，了解當地能源開發與應用的現況，再加上實際勘查大園觀音地區海岸特有生態—藻礁，動手清潔活動區域的海岸沙洲，並搭配地科教育之闖關遊戲做為活動之串連，以宣導重視自然災害與環境關懷的重要性，將環境關懷付諸行動、實際的去注意、去關心自身所處的生活環境，進而能夠珍惜現有的生活資源，愛護寶貴而美麗的地球。
