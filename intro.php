			<p><br>
				<a style="font-weight:bold;font-size:16pt;">緣起：</a><br>
					所謂服務學習就是「服務」與「學習」，也就是在「服務」過程中獲得「學習」 的效果。
					服務學習的目的，除在提供學生獲得實際經驗的機會，達到真正的學以致用，藉實際服務與知識技能的融合，
					得以促進自我的成長外，更經由服務社會，滿足社會之潛在需求，使得學校與社會的關係更加密切。<br>
					本校於 2008 年 4 月 22 日經校務會議通過「國立中央大學服務學習施行辦法」，
					訂定 97 學年度起，所有學士班學生必須通過服務學習課程及學生學習護照學習，方可畢業。
					透過本計畫的執行，期望學生在從事服務時，了解服務學習真正意涵，提 升學生服務品質與效能。<br>
					因此，為了以更有效的行動關懷弱勢、關懷社會、關懷這塊土地與世界。
					在郝玲妮學務長的指示下，於 2010 年 3 月特別設置「服務學習中心」，
					以期透過有系統、 持續地推動「服務學習」課程與專案，讓中大的教職員生可以透過專業、發揮創意； 
					同時，在「從做中學」的實務經驗中，推動志願服務及品德教育的各項培訓與體驗學習，
					導引正向的人生價值態度，形塑中大優質學習輔導環境，讓中大的教職員生體認如何面對人生的困難與失敗，
					對於生命更加珍惜，建立本校服務學習的優良風氣。<br><br>
					<a style="font-weight:bold;font-size:16pt;">願景：</a><br>
					將持續以「服務學習」必修課程為經，並與校園活動「品格教育」為緯，融合「正式課程」與「非正式課程」，
					提供更全面的情意學習環境。並從服務學習教育、 班級經營與成長、社會關懷與服務到服務學習，
					不僅希望能增進學生學習的廣度， 同時能增加學習的深度，從認知、情意、技術、體驗服務與學習的教學理念，
					倡導 推動「服務、學習」的理念已成為極為適合與重要的教學策略。基於服務學習的理 念實踐，積極推動服務學習方案，
					期望藉由系統化、計畫性與整體性的規劃、省思、 評量與修正，協助『服務學習』課程計畫之推動，體驗服務的情操，
